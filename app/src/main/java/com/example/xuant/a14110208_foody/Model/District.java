public class District {
     int id, city_id, cout_street;
     String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCity_id() {
        return city_id;
    }

    public void setCity_id(int city_id) {
        this.city_id = city_id;
    }

    public int getCout_street() {
        return cout_street;
    }

    public void setCout_street(int cout_street) {
        this.cout_street = cout_street;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
